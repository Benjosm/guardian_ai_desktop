#include <iostream>

int main() {
    std::cout << "Hello, World!" << std::endl;

    // Basic loop and arithmetic to test debugging
    for (int i = 0; i < 5; ++i) {
        int result = i * i;
        std::cout << "Square of " << i << " is " << result << std::endl;
    }

    // Return a success code
    return 0;
}
